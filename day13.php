   <h2>4. Подьем штанги на бицепс </h2>
 <div class="container w">
		<div class="row centered">
			<div class="col-lg-6"><img src="img/podem-shtangi-na-biceps-obratnym-xvatom.jpg" alt="Подьем штанги на бицепс"></div>
			<div class="col-lg-6"><img src="img/048689001523434426.gif" alt="Подьем штанги на бицепс"></div>
			<h2>Техника выполнения</h2>
			<div class="col-lg-6"><h3>Снарядите штангу и возьмите ее хватом на ширине плеч, ноги поставьте также. Встаньте ровно и слегка подсогните колени. Ладони должны быть обращены вперед, а локти находится вблизи туловища. статически напрягите мышцы брюшного пресса. Это Ваше исходное положение.</h3>
			</div>
			<div class="col-lg-6"><h3>Удерживая плечи в неподвижной позиции, начните поднимать вес вверх (скручивать его вперед) только за счет движения предплечий и тягового усилия бицепсов. Продолжайте движение до тех пор, пока бицепсы полностью не сократятся, и штанга не окажется на уровне плеч. Задержитесь на 1-2 счета в конечной точке амплитуды. Техника дыхания - при опускании снаряда вдох, при подъеме – выдох.</h3></div>
