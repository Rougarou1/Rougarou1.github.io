		<div class="col-lg-12"><h3>Подойдите к тренажеру верхний блок и отрегулируйте высоту коленных подушечек. Сядьте в тренажер, поверните руки ладонями к себе, вытяните их вверх и ухватитесь за планку. Отклоните корпус несколько назад, создавая естественную арку в нижней части спины. Взгляд направьте вперед и вверх. Это Ваша исходная позиция.



Вдохните и на выдохе потяните руки вниз и назад, создавая тяговое усилие за счет мышц спины и отведения дельт/верха руки вниз и назад, планку к себе до касания ее верха груди. Задержитесь в сокращенной позиции на 1-2 счета и медленно и подконтрольно на вдохе распрямите руки, растянув широчайшие. Повторите заданное количество раз.
