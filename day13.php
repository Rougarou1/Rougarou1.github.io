<h2>3. Тяга Хаммера на спину </h2>
 <div class="container w">
		<div class="row centered">
			<div class="col-lg-6"><img src="img/TyagagorizontalnyyTrenazher.jpg" alt="Тяга Хаммера на спину"></div>
			<div class="col-lg-6"><img src="img/shir7.jpg" alt="Тяга Хаммера на спину">
			</div>
			<h2>Техника выполнения</h2>
			<div class="col-lg-12"><h3>1.Направьте кисти к поясу. Расправьте плечи, на расстоянии вытянутых рук возьмитесь наиболее удобных для вас хватом за тренажер (узким или широким).
2. Используя широкий хват, помните о том, что ваши локти должны расставляться в разные стороны, в то время, как при использовании узкого хвата они должны быть очень плотно прижаты к вашему туловищу.
3. Начинайте тянуть ручки тренажера к себе, не совершая попутно никаких лишних движений и не наклоняясь.
4. Постарайтесь свести лопатки ровно настолько, насколько это возможно в максимальной точке.
5. Во время выполнения подхода не расслабляйтесь, держите мышцы собранными.
6. Выполняя поочередную рычажную тягу в хаммере, используйте узкий хват.</h3>
			</div>
