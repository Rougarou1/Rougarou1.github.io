<h2>1. Подтягивания </h2>
 <div class="container w">
		<div class="row centered">
			<div class="col-lg-4"><img src="img/Biceps.jpg" alt="Техника подтягиваний"></div>
			<div class="col-lg-4"><img src="img/250px-Atlas_fitnesa38.jpg" alt="Техника подтягиваний"></div>
			<div class="col-lg-4"><iframe width="300" height="225" src="https://www.youtube.com/embed/pRFar1efVLM" frameborder="0" allow="autoplay; encrypted-media" allowfullscreen></iframe></div>
			<div class="col-lg-12"><h3>Подтягивания - это упражнение, чаще всего выполняемое на турнике; рабочие группы мышц: комплекс мышц рук (главным образом,предплечья, бицепсы) и спины (широчайшие). Чем шире хват руками за перекладину, тем больше нагрузка ложится на широчайшие мышцы спины; чем уже хват – тем больше нагрузка ложится на бицепсы. Если подтягиваться, касаясь перекладины затылком, то широчайшие мышцы растягиваются в ширину. А если подтягиваться, касаясь подбородком, то широчайшие растягиваются ещё и в толщину. При узком хвате, ладонями к себе, дополнительную нагрузку получает нижняя часть широчайших, находящаяся в районе талии.</h3>
			</div>
			<h2>Техника подтягиваний</h2>
			<div class="col-lg-12"><h3>Ухватитесь за турник верхним хватом, руки чуть шире ширины плеч. Повисните на перекладине. Далее подтянитесь, при этом постарайтесь коснуться верхней частью груди перекладины. Сделайте паузу на секунду в верхней точке амплитуды, а затем плавно вернитесь в стартовое положение.

Чем шире используется хват в подтягивании, тем больше задействованы в работе круглые мышцы, благодаря которым обеспечивается расширение спины. Хват для подтягивания должен быть с ладонями направленными от вас. По возможности лучше использовать устойчивую скамейку или другую подставку, высота которой отрегулирована таким образом, чтобы перекладина оказалась на уровне груди. Удерживая перекладину, спуститесь со скамейки, согните колени и плавно опуститесь в нижнюю точку, обеспечив максимальную растяжку верхней части спины. Перед тем, как подняться, сделайте паузу на 2-3 секунды, чтобы широчайшие мышцы как следует растянулись перед напряжением.</h3>
	<div class="col-lg-12"><h3><i class="fa fa-exchange" aria-hidden="true"> </i> Подходов : 5-6</h3><h3><i class="fa fa-refresh" aria-hidden="true"></i> Повторов : MAX(запишите свои повторения в блокнот)</h3> <h3><i class="fa fa-clock-o" aria-hidden="true"> </i> Отдых : 2 мин.</h3></div>
